package buchKapitel04;

public class Punkt5 {
	
	private double x;
	private double y;
	
	// Konstruktor
	public Punkt5 (double x, double y) {
		this.x = x;
		this.y = y;
	}
	
	public double getX() {
		return x;
	}
	
	public void setX (double u) {
		x = u;
	}
	
	public double getY() {
		return y;
	}
	
	public void setY (double v) {
		y = v;
	}
}